function drawChessboard(size) {
var chessBoard = "";
for (let i = 0; i < size; i++){
  for (let j = 0; j < size; j++){
      if((i + j) % 2 === 0){
        chessBoard += ' ';
      } else {
        chessBoard += '#';
      }
    }
    chessBoard += '\n';
  } // after my inner loop, use new line character
console.log(chessBoard);
}
 

